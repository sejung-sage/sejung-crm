-- 0024_classes_backfill_close_paren_graduated.sql
-- "종)" prefix (앞 괄호 누락된 변형) 종강 강좌의 end_date 일괄 백필.
--
-- 배경:
--   0022 에서 "(종)" prefix 종강 강좌의 end_date NULL 행을 어제로 백필했으나,
--   2026-05-06 진단 결과 종강 prefix 변형이 두 가지 존재함을 확인:
--     - "(종)..." prefix : 1,993건 (정상 닫힌 괄호) — 0022 에서 처리됨
--     - "종)..."   prefix :   197건 (앞 괄호 누락 변형) — 본 마이그 대상
--
--   list-classes 의 진행/종강 가드도 두 변형을 모두 인식하도록 동시에 갱신.
--
-- 변경:
--   name 이 '종)' 으로 시작하면서 end_date 가 NULL 인 행의 end_date 를
--   '어제' 로 일괄 채운다. 정확한 종강일은 원본(아카2000)에서 알 수 없으므로
--   "오늘 이전" 임을 보장하는 가장 보수적인 값(어제) 을 선택.
--
--   active 컬럼은 손대지 않는다 — 운영자가 별도 관리하는 미사용 플래그라
--   본 마이그의 책임 범위가 아님.
--
-- 안전성:
--   - UPDATE 만 수행, DELETE / DROP 없음.
--   - 조건이 매우 좁아(이름 prefix + NULL) 의도치 않은 행을 건드릴 위험 낮음.
--   - 멱등 (이미 end_date 가 채워진 행은 WHERE 절에서 빠짐).
--   - 0022 와 동일 패턴 — 운영 검증 완료된 안전망.
--
-- 롤백 (수동):
--   본 마이그 적용 전 NULL 이었음을 별도 보존하지 않으므로 자동 롤백 불가.
--   필요 시 백업 스냅샷에서 복원하거나, 운영자가 단건 단위로 NULL 로 되돌려야 한다.
-- ============================================================

UPDATE classes
SET end_date = (CURRENT_DATE - INTERVAL '1 day')::date
WHERE end_date IS NULL
  AND name LIKE '종)%';
