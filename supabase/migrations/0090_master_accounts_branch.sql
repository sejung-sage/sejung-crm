-- ============================================================
-- 0090_master_accounts_branch.sql
-- 마스터 계정의 분원을 "마스터"로 통일.
-- ------------------------------------------------------------
-- 마스터는 특정 분원에 속하지 않는데 기존 데이터는 '대치' 등으로 채워져
-- 계정 목록 분원 컬럼에 잘못 표기됐다. 마스터 전용 "마스터" 분원으로 갱신.
--
-- 안전:
--   - role='master' 행만 대상. branch 가 이미 '마스터'면 건드리지 않음(멱등).
--   - branch 는 RLS 데이터 격리에 마스터에겐 무의미(is_master() 가 분원 무시)
--     하므로 데이터 접근 범위에 영향 없음. 순수 표시용 정정.
-- ============================================================

BEGIN;

UPDATE public.crm_users_profile
   SET branch = '마스터',
       updated_at = NOW()
 WHERE role = 'master'
   AND branch <> '마스터';

COMMIT;
