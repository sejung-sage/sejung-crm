-- ============================================================
-- 0028_classes_backfill_pye_graduated.sql
-- "(폐)" / "폐)" prefix 폐강 강좌의 end_date 일괄 백필.
--
-- 배경:
--   0022(`(종)%`) / 0024(`종)%`) 에 이어 폐강 prefix 도 동일하게 처리.
--   2026-05-07 진단 결과:
--     - "(폐)..."  8건  (NULL 7건)
--     - "폐)..."  10건  (NULL 6건)
--   본 마이그 대상 = 두 prefix 합쳐 NULL 13건.
--
-- 변경:
--   name 이 '(폐)' 또는 '폐)' 로 시작하면서 end_date 가 NULL 인 행의
--   end_date 를 '어제' 로 일괄 채운다. 정확한 폐강일은 원본에서 알 수 없으므로
--   "오늘 이전" 임을 보장하는 가장 보수적인 값(어제) 선택.
--
--   list-classes 코드 가드는 GRADUATED_PREFIXES = [(종), 종), (폐), 폐)] 4종
--   으로 영구 가드 — 향후 동기화로 NULL 인 폐강 강좌가 다시 들어와도 진행 중
--   탭에 노출되지 않음.
--
-- 안전성:
--   - UPDATE 만 수행, DELETE / DROP 없음.
--   - 조건이 매우 좁아(이름 prefix + NULL) 의도치 않은 행 위험 낮음.
--   - 멱등 (이미 end_date 가 채워진 행은 WHERE 절에서 빠짐).
--   - 0022 / 0024 와 동일 패턴 — 운영 검증된 안전망.
--
-- 롤백 (수동):
--   본 마이그 적용 전 NULL 이었음을 별도 보존하지 않으므로 자동 롤백 불가.
--   필요 시 백업 스냅샷에서 복원하거나 단건 단위로 NULL 로 되돌려야 한다.
-- ============================================================

UPDATE classes
SET end_date = (CURRENT_DATE - INTERVAL '1 day')::date
WHERE end_date IS NULL
  AND (name LIKE '(폐)%' OR name LIKE '폐)%');
